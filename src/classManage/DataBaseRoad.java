package classManage;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;

public class DataBaseRoad {
	public void roading(NameList list){
        //声明Connection对象
        Connection con;
        //驱动程序名
        String driver = "com.mysql.jdbc.Driver";
        //URL指向要访问的数据库名mydata
        String url = "jdbc:mysql://localhost:3306/class_manage";
        //MySQL配置时的用户名
        String user = "root";
        //MySQL配置时的密码
        String password = "1722110122";
        //遍历查询结果集
        try {
            //加载驱动程序
            Class.forName(driver);
            //1.getConnection()方法，连接MySQL数据库！！
            con = DriverManager.getConnection(url,user,password);
            if(!con.isClosed())
                System.out.println("Succeeded connecting to the Database!");
            //2.创建statement类对象，用来执行SQL语句！！
            Statement statement = con.createStatement();
            //要执行的SQL语句
            /*
             * 其中 结尾字段为数据表的名称
             */
            String sql = "select * from 17softe1";
            //3.ResultSet类，用来存放获取的结果集！！
            ResultSet rs = statement.executeQuery(sql);

             
            int no = 0;
            String name = null;
            while(rs.next()){
                //获取no字段
                no = rs.getInt("no");
                //获取name字段
                name = rs.getString("name");
                
                list.list.add(new Student(no,name));
                list.copy.add(new Student(no,name));
                list.show.add(new Student(no,name));

                list.num = rs.getRow();
                list.className = rs.getString("class");
            }
            rs.close();
            con.close();
        } catch(ClassNotFoundException e) {   
            //数据库驱动类异常处理
            System.out.println("Sorry,can`t find the Driver!");   
            e.printStackTrace();   
            } catch(SQLException e) {
            //数据库连接失败异常处理
            e.printStackTrace();  
            }catch (Exception e) {
            // TODO: handle exception
            e.printStackTrace();
        }	
	}
}
